#To define a floating point number, you may use one of the following notations:
#floating point number isn't that icecream called float, it's decimal number in simple words.

myfloat = 0.96
print(myfloat) #used for putting decimal before the number.
#Output will be: 0.96

myfloat = float(69)
print(myfloat) #used for putting decimal after the number
#Output will be: 69.0

